package egovframework.com.common.help;

public class HelpItemVO {

    private String itemId;
    private String pageId;
    private String title;
    private String body;
    private String anchorSelector;
    private Integer displayOrder;
    private String activeYn;
    private String placement;
    private String imageUrl;
    private String iconName;
    private String highlightStyle;
    private String ctaLabel;
    private String ctaUrl;

    public String getItemId() { return itemId; }
    public void setItemId(String itemId) { this.itemId = itemId; }
    public String getPageId() { return pageId; }
    public void setPageId(String pageId) { this.pageId = pageId; }
    public String getTitle() { return title; }
    public void setTitle(String title) { this.title = title; }
    public String getBody() { return body; }
    public void setBody(String body) { this.body = body; }
    public String getAnchorSelector() { return anchorSelector; }
    public void setAnchorSelector(String anchorSelector) { this.anchorSelector = anchorSelector; }
    public Integer getDisplayOrder() { return displayOrder; }
    public void setDisplayOrder(Integer displayOrder) { this.displayOrder = displayOrder; }
    public String getActiveYn() { return activeYn; }
    public void setActiveYn(String activeYn) { this.activeYn = activeYn; }
    public String getPlacement() { return placement; }
    public void setPlacement(String placement) { this.placement = placement; }
    public String getImageUrl() { return imageUrl; }
    public void setImageUrl(String imageUrl) { this.imageUrl = imageUrl; }
    public String getIconName() { return iconName; }
    public void setIconName(String iconName) { this.iconName = iconName; }
    public String getHighlightStyle() { return highlightStyle; }
    public void setHighlightStyle(String highlightStyle) { this.highlightStyle = highlightStyle; }
    public String getCtaLabel() { return ctaLabel; }
    public void setCtaLabel(String ctaLabel) { this.ctaLabel = ctaLabel; }
    public String getCtaUrl() { return ctaUrl; }
    public void setCtaUrl(String ctaUrl) { this.ctaUrl = ctaUrl; }
}
