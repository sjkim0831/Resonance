#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const storePath = path.join(root, "projects/carbonet-backend-metadata/builder/platform-builder-store.json");
const menuPath = path.join(root, "apps/carbonet-api/src/main/resources/db/baseline/admin-menu-169.tsv");
const outputPath = process.argv[2] || path.join(root, "var/run/sdui-page-manifest-sync.sql");

const quote = (value) => `'${String(value ?? "").replaceAll("'", "''")}'`;
const store = JSON.parse(fs.readFileSync(storePath, "utf8"));
const pages = (store.assets?.pages || [])
  .filter((page) => page.routePath === "/admin/system" || page.routePath.startsWith("/admin/system/"))
  .sort((left, right) => left.routePath.localeCompare(right.routePath));

const menuByRoute = new Map();
for (const line of fs.readFileSync(menuPath, "utf8").split(/\r?\n/)) {
  if (!line.trim()) continue;
  const [menuCode, menuName, , routePath] = line.split("\t");
  if (!routePath?.startsWith("/admin/system")) continue;
  const previous = menuByRoute.get(routePath);
  if (!previous || (menuCode.startsWith("A006") && !previous.menuCode.startsWith("A006"))) {
    menuByRoute.set(routePath, { menuCode, menuName });
  }
}

const values = pages.map((page) => {
  const menu = menuByRoute.get(page.routePath);
  const schema = JSON.stringify({
    schemaVersion: 1,
    runtimeMode: "source-backed",
    sourcePath: page.sourcePath,
    exportName: page.exportName,
    capabilities: page.capabilities || {},
    registryStatus: page.status
  });
  return `(${[
    quote(`system:${page.pageId}`), quote(menu?.menuName || page.title), quote(page.routePath), quote("admin"),
    menu ? quote(menu.menuCode) : "NULL", quote("v1"), quote("krds-current"), quote("Y"),
    "CURRENT_TIMESTAMP", "CURRENT_TIMESTAMP", quote(page.title), quote(page.routePath), quote(schema), quote("DRAFT")
  ].join(", ")})`;
});

if (!values.length) throw new Error("No system pages found in the builder asset registry");
fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, [
  "-- Generated by ops/scripts/sync-sdui-page-manifests.mjs. Existing manifests are preserved.",
  "INSERT INTO ui_page_manifest (page_id, page_name, route_path, domain_code, menu_code, layout_version, design_token_version, active_yn, created_at, updated_at, page_title, page_url, component_schema, version_status)",
  "SELECT candidate.* FROM (VALUES",
  values.join(",\n"),
  ") AS candidate(page_id, page_name, route_path, domain_code, menu_code, layout_version, design_token_version, active_yn, created_at, updated_at, page_title, page_url, component_schema, version_status)",
  "WHERE NOT EXISTS (SELECT 1 FROM ui_page_manifest existing WHERE existing.route_path = candidate.route_path)",
  "ON CONFLICT (page_id) DO NOTHING;",
  "DELETE FROM ui_page_manifest generated",
  "WHERE generated.page_id LIKE 'system:%'",
  "  AND EXISTS (",
  "    SELECT 1 FROM ui_page_manifest preserved",
  "    WHERE preserved.route_path = generated.route_path",
  "      AND preserved.page_id <> generated.page_id",
  "      AND preserved.page_id NOT LIKE 'system:%'",
  "  );",
  ""
].join("\n"), "utf8");
console.log(JSON.stringify({ outputPath, candidates: pages.length, menuMatched: pages.filter((page) => menuByRoute.has(page.routePath)).length }));
